def get_db_connection():
    conn_str = (
        f"DRIVER={{ODBC Driver 17 for SQL Server}};"
        f"SERVER={DB_SERVER};"
        f"DATABASE={DB_DATABASE};"
        f"Trusted_Connection=yes;"
    )
    return pyodbc.connect(conn_str)

def update_meaning(conn, word, meanings, pos):
    """更新单词的多释义"""
    cursor = conn.cursor()
    try:
        meaning_json = json.dumps(meanings, ensure_ascii=False)
        # 取第一个释义作为 meaning_cn
        main_meaning = meanings[0] if meanings else ""

        cursor.execute("""
            UPDATE tb_word
            SET meaning_trans = ?, meaning_cn = ?
            WHERE word = ?
        """, (meaning_json, main_meaning, word))
        conn.commit()
        return cursor.rowcount > 0
    except Exception as e:
        return False

def main():
    print("=" * 60)
    print("     多释义扩展工具")
    print("=" * 60)

    conn = get_db_connection()
    try:
        print("[OK] 数据库连接成功\n")

        total = len(MULTIPLE_MEANINGS)
        updated = 0
        skipped = 0
        not_found = 0

        print(f"准备更新 {total} 个常用词的多释义...\n")

        for i, (word, (meanings, pos)) in enumerate(MULTIPLE_MEANINGS.items()):
            # 检查单词是否存在
            cursor = conn.cursor()
            cursor.execute("SELECT id, meaning_trans FROM tb_word WHERE word = ?", word)
            result = cursor.fetchone()

            if not result:
                print(f"[NOT FOUND] {word}")
                not_found += 1
                continue

            # 检查是否已有多释义
            if result.meaning_trans:
                try:
                    existing = json.loads(result.meaning_trans)
                    if len(existing) > 1:
                        print(f"[SKIP] {word} - 已有多释义")
                        skipped += 1
                        continue
                except Exception:
                    pass

            # 更新多释义
            if update_meaning(conn, word, meanings, pos):
                print(f"[OK] {word}: {meanings}")
                updated += 1
            else:
                print(f"[SKIP] {word} - 更新失败")
                skipped += 1

            if (i + 1) % 10 == 0:
                time.sleep(0.5)

        print("\n" + "=" * 60)
        print("     更新完成!")
        print("=" * 60)
        print(f"  总计：{total} 个")
        print(f"  成功：{updated} 个")
        print(f"  跳过：{skipped} 个")
        print(f"  未找到：{not_found} 个")
        print("=" * 60)
    finally:
        conn.close()

if __name__ == "__main__":
    main()
